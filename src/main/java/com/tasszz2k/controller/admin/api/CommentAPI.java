    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.setCharacterEncoding("UTF-8");
        response.setContentType("application/json");

        ObjectMapper mapper = new ObjectMapper();
        CommentModel commentModel = HttpUtils.of(request.getReader()).toModel(CommentModel.class);
//        newsModel.setCreatedBy(((UserModel) SessionUtil.getInstance().getValue(request, "USERMODEL")).getUserName());
        commentModel = commentService.save(commentModel);
        mapper.writeValue(response.getOutputStream(), commentModel);

    }

